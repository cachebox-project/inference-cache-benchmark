"""
dumb_gateway_client.py — minimal HTTP gateway that models production routing.

What it does
------------
genai-bench expects a single OpenAI-compatible HTTP endpoint. inference-cache's
production gateway is intentionally dumb: tokenize, hash, ask the server, route.
This client mirrors that contract — nothing more (CAC-152).

  1. Tokenize the prompt with the configured HF tokenizer (Llama-3.1 chat
     template for chat-completions, raw encode for completions).
  2. Chunk the token sequence into ``block_size``-token blocks and emit
     content-addressed block hashes (XXH3-64 rolling prefix hash, seed 1337),
     byte-identical to the inference-cache subscriber and SMG.
  3. Call ``LookupRoute(tenant, model, block_hashes, block_token_counts)``.
  4. Route:
       * ``PREFIX_MATCH`` → ``replica_scores[0].replica_id`` (resolved to URL)
       * ``NO_HINT`` / ``TIMEOUT`` / ``TENANT_HOT`` / unknown replica id /
         RPC error → round-robin across all configured replicas (CAC-154).

That is the whole logic. There is no per-replica chain table, no ZMQ
subscription, no chain walk, no ROUTER replay — those exist only in the legacy
proxy (``lookup_proxy_legacy.py``) and were never part of the production
gateway model. See CAC-152 for the design rationale and PROJECT_CONTEXT.md
§1 "we decide routing; the gateway follows".

Modes (``--routing-mode``)
-------------------------
* ``lookup`` (default) — call LookupRoute and honor PREFIX_MATCH; fall back
  to round-robin on any other reason code. Models the full production path.
* ``round-robin`` — skip LookupRoute entirely; every request round-robins
  across ``--replica`` URLs. Tokenizer and gRPC channel are not loaded.
  This is the harness's no-hint mode (CAC-153) — measures the cache plane
  with the routing layer disabled.

Usage
-----
    python3 dumb_gateway_client.py \
      --listen 0.0.0.0:18100 \
      --ic-server localhost:38002 \
      --replica r0=http://localhost:38010 \
      --replica r1=http://localhost:38011 \
      --replica r2=http://localhost:38012 \
      --tokenizer unsloth/Meta-Llama-3.1-8B-Instruct \
      --tenant ic-smoke \
      --routing-mode lookup \
      --log /tmp/dumb_gateway_client.log

Requires the inference-cache proto stubs on ``sys.path`` — generated by
``make proto`` (sibling inference-cache repo expected).
"""

from __future__ import annotations

import argparse
import asyncio
import json
import logging
import struct
import sys
import time
from typing import Dict, List, Optional, Tuple

import aiohttp
import xxhash
from aiohttp import web

try:
    import grpc

    from inferencecache.v1alpha1 import inferencecache_pb2 as pb
    from inferencecache.v1alpha1 import inferencecache_pb2_grpc as pb_grpc
except ImportError as e:  # pragma: no cover
    sys.stderr.write(
        f"Could not import inference-cache proto stubs: {e}\n"
        "Generate them with `make proto` (sibling inference-cache repo expected)\n"
        "or set PYTHONPATH to the directory containing inferencecache/v1alpha1/.\n"
    )
    sys.exit(2)

try:
    from transformers import AutoTokenizer
except ImportError as e:  # pragma: no cover
    sys.stderr.write(
        f"Could not import transformers: {e}\nInstall with: make install\n"
    )
    sys.exit(2)


DEFAULT_HASH_SCHEME = "vllm"
DEFAULT_BLOCK_SIZE = 16
DEFAULT_LOOKUP_TIMEOUT_S = 0.5
DEFAULT_TOKENIZER = "unsloth/Meta-Llama-3.1-8B-Instruct"


_XXH3_SEED = 1337


def _content_hash(block_tokens: List[int]) -> int:
    """Position-independent XXH3-64 (seed 1337) over the little-endian u32
    encoding of each token. Matches the inference-cache subscriber / SMG
    ``compute_content_hash``."""
    buf = b"".join(struct.pack("<I", t & 0xFFFFFFFF) for t in block_tokens)
    return xxhash.xxh3_64_intdigest(buf, seed=_XXH3_SEED)


def _next_seq_hash(prev: int, content: int) -> int:
    """Roll the prefix hash forward one block: XXH3-64(seed 1337) over
    ``prev.le8 ++ content.le8`` (matches SMG ``compute_next_seq_hash``)."""
    return xxhash.xxh3_64_intdigest(
        struct.pack("<Q", prev) + struct.pack("<Q", content), seed=_XXH3_SEED
    )


def _hash_blocks(
    token_ids: List[int], block_size: int
) -> Tuple[List[bytes], List[int]]:
    """Chunk ``token_ids`` into ``block_size``-token blocks and content-address them.

    Each block's key is the rolling positional prefix hash:
    ``prefix[0] = content_hash(block_0)`` and
    ``prefix[i] = xxh3(prefix[i-1].le8 || content_hash(block_i).le8)`` — XXH3-64
    with seed 1337, serialized 8-byte big-endian. This is byte-identical to the
    inference-cache subscriber's ingest hash and SMG's ``event_tree.rs``, so the
    server's index (keyed on the subscriber's hash) matches what we send here.
    Partial trailing tokens (< block_size) are dropped so the chain only contains
    blocks the engine would have committed to its index.

    Why content-addressed: the gateway recomputes hashes from the prompt alone —
    no engine-event observation required. The engine's own block hash is seeded
    per-process (vLLM NONE_HASH) and so is NOT reproducible; this content
    fingerprint is, which is exactly why the index keys on it.
    """
    block_hashes: List[bytes] = []
    block_counts: List[int] = []
    n_full = (len(token_ids) // block_size) * block_size
    prev: Optional[int] = None
    for i in range(0, n_full, block_size):
        content = _content_hash(token_ids[i : i + block_size])
        prefix = content if prev is None else _next_seq_hash(prev, content)
        block_hashes.append(prefix.to_bytes(8, "big"))
        block_counts.append(block_size)
        prev = prefix
    return block_hashes, block_counts


def _classify_match_quality(chain_blocks: int) -> Optional[str]:
    """Bucket a chain length for log-grepping.

    None for chain_blocks == 0 (NO_HINT etc. — bucket doesn't apply).
    Buckets match the legacy proxy so existing log-analysis tooling
    (correlate.py, brown-bag scripts) keeps working unmodified.
    """
    if chain_blocks <= 0:
        return None
    if chain_blocks == 1:
        return "trivial"
    if chain_blocks <= 7:
        return "weak"
    return "strong"


def _parse_replica(spec: str) -> Tuple[str, str]:
    """Parse ``id=url`` or legacy ``id|zmq|url[|router]`` → (id, http_url).

    The legacy pipe form is accepted so operators can keep their existing
    ``LOOKUP_PROXY_REPLICAS`` env var unchanged when switching modes; the
    ZMQ field is parsed and ignored (dumb client does no ZMQ).
    """
    if "=" in spec:
        rid, _, url = spec.partition("=")
    elif "|" in spec:
        parts = spec.split("|")
        if len(parts) < 3 or not parts[0] or not parts[2]:
            raise argparse.ArgumentTypeError(
                f"--replica legacy pipe form needs id|zmq|http_url[|router], got: {spec!r}"
            )
        rid, url = parts[0], parts[2]
    else:
        raise argparse.ArgumentTypeError(
            f"--replica spec must be 'id=url' or 'id|zmq|http_url[|router]', got: {spec!r}"
        )
    if not rid or not url or "://" not in url:
        raise argparse.ArgumentTypeError(
            f"--replica id and url must be non-empty and url must include a scheme, got: {spec!r}"
        )
    return rid, url.rstrip("/")


class DumbGatewayClient:
    def __init__(
        self,
        ic_server: str,
        replicas: List[Tuple[str, str]],
        tokenizer_name: str,
        hash_scheme: str = DEFAULT_HASH_SCHEME,
        block_size: int = DEFAULT_BLOCK_SIZE,
        tenant: str = "default",
        routing_mode: str = "lookup",
        lookup_timeout_s: float = DEFAULT_LOOKUP_TIMEOUT_S,
    ):
        if not replicas:
            raise ValueError("replicas must be a non-empty list")
        if routing_mode not in ("lookup", "round-robin"):
            raise ValueError(f"routing_mode must be lookup|round-robin, got {routing_mode!r}")
        self.ic_server = ic_server
        # Preserve order so round-robin is deterministic across processes.
        self.replicas: List[Tuple[str, str]] = list(replicas)
        self.replica_url_by_id: Dict[str, str] = dict(replicas)
        self.tenant = tenant
        self.hash_scheme = hash_scheme
        self.block_size = block_size
        self.routing_mode = routing_mode
        self.lookup_timeout_s = lookup_timeout_s

        if routing_mode == "lookup":
            logging.info("loading tokenizer: %s", tokenizer_name)
            self.tokenizer = AutoTokenizer.from_pretrained(tokenizer_name)
            logging.info("tokenizer loaded: vocab_size=%d", len(self.tokenizer))
        else:
            self.tokenizer = None
            logging.info("routing-mode=round-robin; skipping tokenizer load")

        self._channel: Optional["grpc.aio.Channel"] = None
        self._stub: Optional[pb_grpc.InferenceCacheStub] = None
        self._client_session: Optional[aiohttp.ClientSession] = None

        # Per-process round-robin pointer — CAC-154 fallback spread.
        self._rr_counter = 0
        self.stats = {
            "requests_total": 0,
            "lookup_prefix_match": 0,
            "lookup_no_hint": 0,
            "lookup_tenant_hot": 0,
            "lookup_timeout": 0,
            "lookup_errors": 0,
            "lookup_unknown_hint": 0,
            "lookup_short_prompt": 0,
            "lookup_disabled": 0,
            "routed_to_hint": 0,
            "routed_round_robin_fallback": 0,
            "lookup_latency_us_sum": 0.0,
            "match_quality_trivial": 0,
            "match_quality_weak": 0,
            "match_quality_strong": 0,
        }

    async def setup(self) -> None:
        if self.routing_mode == "lookup":
            self._channel = grpc.aio.insecure_channel(self.ic_server)
            self._stub = pb_grpc.InferenceCacheStub(self._channel)
        self._client_session = aiohttp.ClientSession()

    async def teardown(self) -> None:
        if self._channel is not None:
            await self._channel.close()
        if self._client_session is not None:
            await self._client_session.close()

    # ---- tokenize ----------------------------------------------------------

    def _extract_prompt_tokens(self, payload: dict) -> List[int]:
        """Convert an OpenAI request payload to token IDs.

        Mirrors vLLM's tokenization path so the resulting block hashes
        agree byte-for-byte with what a content-addressed engine emits.
        Two payload shapes:
          /v1/completions       → {"prompt": "raw text"}
          /v1/chat/completions  → {"messages": [{"role":..., "content":...}]}
        """
        assert self.tokenizer is not None
        prompt = payload.get("prompt")
        if prompt is not None:
            if isinstance(prompt, list):
                prompt = "\n".join(str(p) for p in prompt)
            return list(self.tokenizer.encode(str(prompt), add_special_tokens=True))
        messages = payload.get("messages") or []
        if messages:
            try:
                rendered = self.tokenizer.apply_chat_template(
                    messages, tokenize=False, add_generation_prompt=True
                )
                return list(self.tokenizer.encode(rendered, add_special_tokens=False))
            except Exception as e:
                logging.warning("apply_chat_template failed (%s); falling back to concat", e)
                text = "\n".join(m.get("content", "") for m in messages)
                return list(self.tokenizer.encode(text, add_special_tokens=True))
        logging.warning(
            "request body has neither 'prompt' nor 'messages'; keys=%s",
            list(payload.keys()),
        )
        return []

    # ---- lookup ------------------------------------------------------------

    async def _lookup_route(
        self, model: str, block_hashes: List[bytes], block_counts: List[int]
    ) -> Tuple[Optional[str], str]:
        """Call LookupRoute. Returns (hint_replica_id, reason_code).

        ``hint_replica_id`` is set only on PREFIX_MATCH; all other reason
        codes (including TENANT_HOT) drop to round-robin per the production
        gateway contract.
        """
        assert self._stub is not None
        t0 = time.perf_counter()
        try:
            req = pb.LookupRouteRequest(
                tenant_id=self.tenant,
                model_id=model,
                hash_scheme=self.hash_scheme,
                block_hashes=block_hashes,
                block_token_counts=block_counts,
            )
            resp = await asyncio.wait_for(
                self._stub.LookupRoute(req), timeout=self.lookup_timeout_s
            )
        except asyncio.TimeoutError:
            self.stats["lookup_timeout"] += 1
            return None, "TIMEOUT"
        except grpc.aio.AioRpcError as e:
            self.stats["lookup_errors"] += 1
            logging.warning("lookup_route_rpc_error: %s", e)
            return None, "ERROR"
        finally:
            self.stats["lookup_latency_us_sum"] += (time.perf_counter() - t0) * 1e6

        rc = resp.reason_code or "NO_HINT"
        if rc == "PREFIX_MATCH" and resp.replica_scores:
            self.stats["lookup_prefix_match"] += 1
            return resp.replica_scores[0].replica_id, rc
        if rc == "NO_HINT":
            self.stats["lookup_no_hint"] += 1
        elif rc == "TENANT_HOT":
            self.stats["lookup_tenant_hot"] += 1
        return None, rc

    # ---- route + forward ---------------------------------------------------

    def _round_robin(self) -> str:
        url = self.replicas[self._rr_counter % len(self.replicas)][1]
        self._rr_counter += 1
        self.stats["routed_round_robin_fallback"] += 1
        return url

    def _pick_upstream(self, hint: Optional[str]) -> Tuple[str, str]:
        """Return (upstream_url, route_reason).

        Hint with a known replica id → that replica's URL ("HINT").
        Hint with an unknown id, or no hint → round-robin ("ROUND_ROBIN").
        """
        if hint is not None:
            url = self.replica_url_by_id.get(hint)
            if url is not None:
                self.stats["routed_to_hint"] += 1
                return url, "HINT"
            self.stats["lookup_unknown_hint"] += 1
        return self._round_robin(), "ROUND_ROBIN"

    async def handle_chat(self, request: web.Request) -> web.StreamResponse:
        self.stats["requests_total"] += 1
        body = await request.read()
        try:
            payload = json.loads(body)
        except Exception:
            return web.Response(status=400, text="malformed JSON")

        model = payload.get("model", "")

        if self.routing_mode == "round-robin":
            self.stats["lookup_disabled"] += 1
            token_ids: List[int] = []
            block_hashes: List[bytes] = []
            hint: Optional[str] = None
            reason = "LOOKUP_DISABLED"
        else:
            token_ids = self._extract_prompt_tokens(payload)
            block_hashes, block_counts = _hash_blocks(token_ids, self.block_size)
            if not block_hashes:
                # Prompt shorter than one block — server can't match anything
                # in the index. Skip the RPC and round-robin directly.
                self.stats["lookup_short_prompt"] += 1
                hint, reason = None, "SHORT_PROMPT"
            else:
                hint, reason = await self._lookup_route(model, block_hashes, block_counts)

        upstream_url, route_reason = self._pick_upstream(hint)

        match_quality = _classify_match_quality(len(block_hashes))
        if match_quality is not None and reason == "PREFIX_MATCH":
            self.stats[f"match_quality_{match_quality}"] += 1

        # Keep the legacy "route_decision" key shape so correlate.py and the
        # brown-bag log greps work unchanged.
        log_extra = {
            "tokens": len(token_ids),
            "chain_blocks": len(block_hashes),
            "lookup_reason": reason,
            "route_reason": route_reason,
            "hint": hint,
            "match_quality": match_quality,
            "upstream": upstream_url,
        }
        logging.info("route_decision %s", log_extra)

        try:
            assert self._client_session is not None
            fwd_headers = {
                k: v for k, v in request.headers.items()
                if k.lower() not in {"host", "content-length"}
            }
            # genai-bench hits both /v1/chat/completions and /v1/completions;
            # forward to whichever path was requested so the upstream sees
            # the same payload shape it would in production.
            upstream_path = request.match_info.get("path") or request.path
            async with self._client_session.post(
                f"{upstream_url}{upstream_path}",
                data=body,
                headers=fwd_headers,
                timeout=aiohttp.ClientTimeout(total=120),
            ) as upstream_resp:
                stream = web.StreamResponse(
                    status=upstream_resp.status, headers=upstream_resp.headers
                )
                stream.headers["X-Cache-Lookup-Reason"] = reason
                stream.headers["X-Cache-Route-Reason"] = route_reason
                await stream.prepare(request)
                async for chunk in upstream_resp.content.iter_chunked(8192):
                    await stream.write(chunk)
                await stream.write_eof()
                return stream
        except Exception as e:
            logging.exception("forward failed")
            return web.Response(status=502, text=f"upstream error: {e}")

    async def handle_metrics(self, _: web.Request) -> web.Response:
        return web.json_response({
            "client": self.stats,
            "routing_mode": self.routing_mode,
            "replicas": [{"id": rid, "url": url} for rid, url in self.replicas],
        })


async def main_async(args: argparse.Namespace) -> None:
    logging.basicConfig(
        filename=args.log,
        level=logging.INFO,
        format="%(asctime)s %(levelname)s %(message)s",
    )
    client = DumbGatewayClient(
        ic_server=args.ic_server,
        replicas=args.replica,
        tokenizer_name=args.tokenizer,
        hash_scheme=args.hash_scheme,
        block_size=args.block_size,
        tenant=args.tenant,
        routing_mode=args.routing_mode,
        lookup_timeout_s=args.lookup_timeout,
    )
    await client.setup()

    app = web.Application()
    app.router.add_post("/v1/chat/completions", client.handle_chat)
    app.router.add_post("/v1/completions", client.handle_chat)
    app.router.add_get("/proxy/metrics", client.handle_metrics)
    app.router.add_get("/health", lambda _r: web.Response(status=200))

    host, port = args.listen.rsplit(":", 1)
    runner = web.AppRunner(app)
    await runner.setup()
    site = web.TCPSite(runner, host, int(port))
    await site.start()
    logging.info(
        "dumb_gateway_client listening on %s; ic_server=%s; replicas=%s; "
        "routing_mode=%s; hash_scheme=%s; block_size=%d",
        args.listen, args.ic_server,
        [f"{r}={u}" for r, u in args.replica],
        args.routing_mode, args.hash_scheme, args.block_size,
    )

    try:
        await asyncio.Event().wait()
    finally:
        await client.teardown()
        await runner.cleanup()


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--listen", default="0.0.0.0:18100")
    ap.add_argument(
        "--ic-server", default="",
        help="inference-cache-server gRPC endpoint, e.g. localhost:38002. "
             "Required for --routing-mode=lookup; unused for round-robin.",
    )
    ap.add_argument(
        "--replica",
        action="append",
        type=_parse_replica,
        default=[],
        help="Replica config: 'id=url' (preferred) or legacy 'id|zmq|http_url[|router]' "
             "(zmq field is ignored — dumb client does no ZMQ). Repeat per replica. "
             "Server-returned replica_id is matched against id; HTTP traffic forwards to "
             "url. Round-robin fallback rotates across the configured list in order. "
             "Example: '--replica r0=http://localhost:38010 --replica r1=http://localhost:38011'.",
    )
    ap.add_argument(
        "--tokenizer", default=DEFAULT_TOKENIZER,
        help=f"HF tokenizer id. Default {DEFAULT_TOKENIZER!r}. Required for "
             f"--routing-mode=lookup; unused for round-robin.",
    )
    ap.add_argument("--hash-scheme", default=DEFAULT_HASH_SCHEME)
    ap.add_argument(
        "--block-size", type=int, default=DEFAULT_BLOCK_SIZE,
        help="Tokens per KV block. Must match the engine's configured KV block size "
             f"so the chain lines up with the server's index. Default {DEFAULT_BLOCK_SIZE}.",
    )
    ap.add_argument(
        "--tenant", default="default",
        help="Tenant ID for LookupRoute. Must equal the tenant the engine's "
             "kvevent-subscriber writes with (its --tenant-id flag — usually the "
             "engine pod's namespace via $(POD_NAMESPACE)).",
    )
    ap.add_argument(
        "--routing-mode", default="lookup", choices=("lookup", "round-robin"),
        help="lookup (default) — call LookupRoute and honor PREFIX_MATCH. "
             "round-robin — skip the RPC; every request rotates across --replica URLs. "
             "Used by the harness's no-hint mode (CAC-153) to measure the cache plane "
             "with the routing layer disabled.",
    )
    ap.add_argument(
        "--lookup-timeout", type=float, default=DEFAULT_LOOKUP_TIMEOUT_S,
        help="LookupRoute client-side timeout (seconds). Must exceed "
             "CachePolicy.lookupTimeoutMs + network RTT + serialization. "
             f"Default {DEFAULT_LOOKUP_TIMEOUT_S}s.",
    )
    ap.add_argument("--log", default=None)
    args = ap.parse_args()

    if not args.replica:
        ap.error("--replica is required: pass at least one 'id=url' spec.")
    if args.routing_mode == "lookup":
        if not args.ic_server:
            ap.error("--ic-server is required for --routing-mode=lookup")
        if not args.tokenizer:
            ap.error("--tokenizer is required for --routing-mode=lookup")

    try:
        asyncio.run(main_async(args))
    except KeyboardInterrupt:
        pass


if __name__ == "__main__":
    main()
